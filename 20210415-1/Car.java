
public class Car {

	// 변수와 메서드의 집합체를 클래스
	
	// 변수
	String company;
	String model;
	String color;
	int maxSpeed;
	
	// 메서드
	//           메서드 명                  매개변수
	public void setCompany(String company) {
		this.company = company;
	}
	
	public void setModel(String model) {
		this.model = model;
	}
	
	public void setColor(String color) {
		this.color = color;
	}
	
	public void setMaxSpeed(int maxSpeed) {
		this.maxSpeed = maxSpeed;
	}
}
