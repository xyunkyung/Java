
public class Ex14 {

	public static void main(String[] args) {
		
		// 90 이상 A, 80 이상 B, 70 이상 C, 60 이상 D, F
		
		int score = 78;
		if (score >= 90) {
			System.out.println("A");
		}
		else if (score >= 80) {
			System.out.println("B");
		}
		else if (score >= 70) {
			System.out.println("C");
		}
		else if (score >= 60) {
			System.out.println("D");
		}
		else {
			System.out.println("F");
		}

	}

}
