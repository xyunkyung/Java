
public class Ex3 {

	public static void main(String[] args) {
		byte b; // -128 ~ 127 	1byte : 8bit
		b = -128;
		System.out.println(b);
		
		b = 127;
		System.out.println(b);
		
		char c; // 0 ~ 65535	unicode 2byte
		c = 0;
		System.out.println(c);
		
		c = 65535;
		System.out.println(c);
		
		short s; // 2byte
		int i; // -2100000000 ~ 2100000000	4byte
		
		long l; // 8byte
		l = 1000000l;
		
		c = 65;
		System.out.println(c);
		
		c = 97;
		System.out.println(c);
		
		c = 66;
		System.out.println(c);
		
		c = 98;
		System.out.println(c);
		
		c = 'D';
		System.out.println(c);
		
		int ii = c;
		System.out.println(ii);
	}

}
